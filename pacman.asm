;MOVIMENTACAO PACMAN
MOVIMENTO_PACMAN PROC

	MOV BX,MOV_X
	MOV DX,MOV_Y

		
	
	MOV AH,07h
	INT 21h
		
	CMP AL,'a'
	JE ESQUERDA1
		
	CMP AL,'d'
	JE DIREITA1
		
	CMP AL,'s'
	JE BAIXO1
	
	CMP AL,'w'
	JE CIMA1
	
	CMP AL,0dh
	JE FIM
		
ESQUERDA1:
	JMP ESQUERDA
	
DIREITA1:
	JMP DIREITA
	
CIMA1:
	JMP CIMA
	
BAIXO1:
	JMP BAIXO
	
FIM:
	MOV AH,4Ch
	INT 21H		
	
FIME:
	RET
	
ESQUERDA:
	CMP BX,31
	JB FIME
	
	PRINTAR APAGA,00H,BX,DX,16,240	
	MOV BX,MOV_X
	MOV DX, MOV_Y
	SUB BX,16
	
	MOV MOV_X,BX
	
	PRINTAR PACMANFECHADO,44H,BX,DX,16,240
	MOV DX,MOV_Y
	MOV BX,MOV_X
	
	PRINTAR APAGA,00H,BX,DX,16,240
	MOV DX,MOV_Y
	MOV BX,MOV_X
	
	PRINTAR PACMANABERTOESQ,44H,BX,DX,16,240
	MOV DX,MOV_Y
	MOV BX,MOV_X
	
	
FIMD:
	RET
	
DIREITA:
	CMP BX,127
	JA FIMD
	
	PUSH BX
	PUSH DX
	
	PRINTAR APAGA,00H,BX,DX,16,240	
	MOV BX,MOV_X
	MOV DX,MOV_Y
	
	ADD BX,16
	MOV MOV_X,BX
	
	PRINTAR PACMANFECHADO,44H,BX,DX,16,240
	MOV DX,MOV_Y
	MOV BX,MOV_X
	
	PRINTAR APAGA,00H,BX,DX,16,240
	MOV DX,MOV_Y
	MOV BX,MOV_X
	
	PRINTAR PACMANABERTODIR,44H,BX,DX,16,240
	MOV DX,MOV_Y
	MOV BX,MOV_X
	
FIMC:
	RET
	
CIMA:
	CMP DX,33
	JB FIMC
	
	PRINTAR APAGA,00H,BX,DX,16,240	
	MOV BX,MOV_X
	MOV DX, MOV_Y
	
	SUB DX,16
	MOV MOV_Y,DX
	
	PRINTAR PACMANFECHADO,44H,BX,DX,16,240
	MOV DX,MOV_Y
	MOV BX,MOV_X
	
	PRINTAR APAGA,00H,BX,DX,16,240
	MOV DX,MOV_Y
	MOV BX,MOV_X
	
	PRINTAR PACMANABERTODIR,44H,BX,DX,16,240
	MOV DX,MOV_Y
	MOV BX,MOV_X
FIMB:
	RET
	
BAIXO:
	CMP DX,300
	JA FIMB
	
	PRINTAR APAGA,00H,BX,DX,16,240	
	MOV BX,MOV_X
	MOV DX, MOV_Y
	ADD DX,16
	MOV MOV_Y,DX
	
	PRINTAR PACMANFECHADO,44H,BX,DX,16,240
	MOV DX,MOV_Y
	MOV BX,MOV_X
	
	PRINTAR APAGA,00H,BX,DX,16,240
	MOV DX,MOV_Y
	MOV BX,MOV_X
	
	PRINTAR PACMANABERTODIR,44H,BX,DX,16,240
	MOV DX,MOV_Y
	MOV BX,MOV_X
	
MOVIMENTO_PACMAN ENDP

PRINT_VIDA PROC

	PRINTAR VIDA,12D, 220,30, 15,195
	PRINTAR VIDA,12D, 240,30, 15,195	; print dos coraçoes
	PRINTAR VIDA,12D, 260,30, 15,195
	
	RET
	
PRINT_VIDA ENDP

PIXEL PROC
;print de pixel	

	MOV COUNT, 0
	MOV LADO_Y, 0
	MOV LADO_X, 0

COMECO:
	MOV CX, EIXO_X
	ADD CX, LADO_X
	INC LADO_X
	MOV DX, EIXO_Y
	ADD DX, LADO_Y

	MOV BL, [SI]

	CMP BL, 0
	JE CONTINUA

	MOV AH, 0Ch
	INT 10h

CONTINUA:
	INC SI
	INC COUNT

	MOV BX, LARGURA_TOTAL
	CMP LADO_X, BX
	JL CONTINUA2

	MOV LADO_X, 0
	INC LADO_Y

continua2:
	MOV BX, DIMENSAO
	CMP COUNT, BX
	JL COMECO

	RET
PIXEL ENDP
