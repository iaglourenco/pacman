	PRINTAR COMIDA, 44H, 210, 107, 1, 1
	PRINTAR COMIDA, 44H, 215, 107, 1, 1
	PRINTAR COMIDA, 44H, 220, 107, 1, 1
	
	RET
BEMVINDO ENDP
