.CODE                                                                                                                                                             
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	MOV ES,AX
	
	MOV AX, 13h   ;setando o modo de video
	INT 10h

	CALL PULALINHA
	
	CALL BEMVINDO	; mensagens de boas vindas 
	
	LEA DX, DECISAO             
	MOV AH, 9                   
	INT 21H 
	
	MOV AH, 1                   
	INT 21H                      
	
	CALL CLS
	
	CALL USUARIO
	
	CALL CLS
	
	CALL ESCOLHAMAPA
	
	CALL CLS
	
	;MATRIZ, COR, POSICAO_X, POSICAO_Y, LARGURA, TAMANHO
	
	MOV MOV_X,80
	MOV MOV_Y,92
	CALL PRINT_VIDA
	
	
	PRINTAR PACMANABERTODIR,44H,80,92,16,240
	
	LOOP1:
	
	CALL PRINTARMAPA
	
	CALL MOVIMENTO_PACMAN
	
	CALL PRINT_VIDA
	
	
	
	;CALL CLS
	JMP LOOP1
	
SAI:
	MOV AH ,4Ch
	INT 21H
MAIN ENDP
