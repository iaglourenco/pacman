;GUARDA NOME USUARIO
USUARIO PROC
	LEA DX, NOME
	MOV AH,9
	INT 21H
	
	MOV AH, 1
	INT 21H
	
	MOV AH, 2H
	INT 21H
	
	MOV VNOME, AL
	
	RET
USUARIO ENDP
