;ESCOLHA DO MAPA
ESCOLHAMAPA PROC

	LEA DX, ESCOLHAM
	MOV AH,9
	INT 21H
	
	LEA DX, MMAPAFACIL
	MOV AH,9
	INT 21H
	
	LEA DX, MMAPAMEDIO
	MOV AH,9
	INT 21H
	
	LEA DX, MMAPADIFICIL
	MOV AH,9
	INT 21H
	
	MOV AH, 1
	INT 21H
	
	MOV DECISAOMAPA, AL
	
	CMP DECISAOMAPA, 1
	CALL PRINTARMAPA
	
	;CONTINUAÇÃO MAPAS!
	
	RET
ESCOLHAMAPA ENDP
;===========================================================
;ESCOLHA DO MAPAFACIL
PRINTARMAPA PROC

	PRINTAR MAPAFACIL, 1d, 30, 30, 114, 9006	;print do mapa
	
	RET
PRINTARMAPA ENDP
